public class RelationOperator{
	public static void main(String[] args) {
		int a = 8;
		int b = 10;
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a == b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a != b);
		boolean flag = a > b;
		System.out.println(flag);

	}
}