public class array_03{
	public static void main(String[] args) {
		// 1.数组是多个相同数据类型的组合，可以实现对数据的
		// 统一管理
		int arr1[] = {1, 2, 3, 60};
		// 2.数组中的元素可以是任何类型的，但是不能混用
		String arry3[] = {"pengge", "aoge", "dongdi"};
		// 3. 数组创建后，如果没有赋值，有默认值
		// int 0\short 0\byte 0\long 0\
		// float 0.0 \ double 0.0\ boolean false
		short arry4[] = new short[3];
	}
} 